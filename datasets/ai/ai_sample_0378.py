class Person:
    def __init__(self, name, age, gender, address):
        self.name = name
        self.age = age
        self.gender = gender
        self.address = address
    
    def get_name(self):
        return self.name
    
    def get_age(self):
        return self.age 
    
    def get_gender(self):
        return self.gender
    
    def get_address(self):
        return self.address