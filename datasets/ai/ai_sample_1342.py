def ascii_art(inputText):
  # Create dictionary
  ascii_dict = {
    'A' : '    _____  ',
    'B' : ' \/3_  3_\/',
    'C' : '  (__/ \__)',
    'D' : ' \/2_  2_\/',
    'E' : '  (__/ \__)',
    'F' : '  (___/ \__)',
    'G' : '  (__&_/ \_)',
    'H' : '   //\/ \\\\',
    'I' : '    ---/ \_',
    'J' : '(___/ \___)',
    'K' : '\\__- .- \_/',
    'L' : '(__/ \___)',
    'M' : '   =\ \\/ /=',
    'N' : '   =5 \/ 5=',
    'O' : '  (___/ \__)',
    'P' : ' \(___/ \__)',
    'Q' : ' (___/ \___)',
    'R' : ' \(___/ \_/',
    'S' : '(__/ \___)',
    'T' : ' (___/ \___)',
    'U' : '  (__/ \___)',
    'V' : '   \ \/ /',
    'W' : '    \\/ | \\/',
    'X' : '     \/ |',
    'Y' : '     \/ |',
    'Z' : '(__/ \___)'
  }

  pic = ""
  for letter in inputText:
    pic = pic + ascii_dict[letter.upper()] + "\n"
    
  return pic

print(ascii_art("Hello World!")) 

