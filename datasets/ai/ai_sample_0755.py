# Program to print the squares of all 
# the numbers from 1 to the given number 
  
# Positive number taken as an input 
num = 5
  
# Iterate from 1 to num 
for i in range(1, num+1): 
  
    # Calculate the square 
    value = i**2
      
    # Print the value 
    print(value)

# Output:
# 1
# 4
# 9
# 16
# 25